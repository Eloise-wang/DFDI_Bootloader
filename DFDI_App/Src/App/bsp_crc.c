/* 全局变量 */
static uint32_t g_curCrc = 0U;

/* ============================================  API实现  ============================================ */

bool BSP_CRC_Init(void)
{
    g_curCrc = BSP_CRC_SEED_INIT_VALUE;
    return TRUE;
}

bool BSP_CRC_Calculate(const uint8_t *pDataBuf, uint32_t dataLen, uint32_t *pCrc)
{
    uint32_t index = 0U;

    if((NULL == pDataBuf) || (NULL == pCrc))
    {
        return FALSE;
    }

    for(index = 0U; index < dataLen; index++)
    {
        g_curCrc = (g_curCrc >> 8U) ^ g_crc32Table[(g_curCrc ^ ((uint32_t)pDataBuf[index])) & 0xFFU];
    }
    *pCrc = g_curCrc;

    return TRUE;
}

bool BSP_CRC_CalculateOnce(const uint8_t *pDataBuf, uint32_t dataLen, uint32_t *pCrc)
{
    uint32_t index = 0U;

    if((NULL == pDataBuf) || (NULL == pCrc))
    {
        return FALSE;
    }

    g_curCrc = BSP_CRC_SEED_INIT_VALUE;
    for(index = 0U; index < dataLen; index++)
    {
        g_curCrc = (g_curCrc >> 8U) ^ g_crc32Table[(g_curCrc ^ ((uint32_t)pDataBuf[index])) & 0xFFU];
    }
    /* CRC-32 最终异或 */
    *pCrc = g_curCrc ^ 0xFFFFFFFFU;

    return TRUE;
}

bool BSP_CRC_Start(uint32_t *pCrc)
{
    if(NULL == pCrc)
    {
        return FALSE;
    }

    g_curCrc = BSP_CRC_SEED_INIT_VALUE;
    *pCrc = g_curCrc;

    return TRUE;
}

bool BSP_CRC_Append(const uint8_t *pDataBuf, uint32_t dataLen, uint32_t *pCrc)
{
    uint32_t index = 0U;

    if((NULL == pDataBuf) || (NULL == pCrc))
    {
        return FALSE;
    }

    for(index = 0U; index < dataLen; index++)
    {
        g_curCrc = (g_curCrc >> 8U) ^ g_crc32Table[(g_curCrc ^ ((uint32_t)pDataBuf[index])) & 0xFFU];
    }
    *pCrc = g_curCrc;

    return TRUE;
}

bool BSP_CRC_End(uint32_t *pCrc)
{
    if(NULL == pCrc)
    {
        return FALSE;
    }

    /* CRC-32 最终异或 */
    *pCrc = g_curCrc ^ 0xFFFFFFFFU;

    return TRUE;
}

bool BSP_CRC_Deinit(void)
{
    return TRUE;
}
